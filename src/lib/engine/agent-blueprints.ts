// ── IDE-Specific Blueprint Generators ──────────────

/**
 * Main entry point: generate agent team blueprint files for a given IDE.
 * Only produces output for IDEs that support subagent/team configurations.
 */
export function generateAgentBlueprints(
  ideTarget: IDETarget,
  variables: TemplateVariables
): GeneratedFile[] {
  switch (ideTarget) {
    case 'claude-code':
      return generateClaudeCodeBlueprints(variables);
    case 'cursor':
      return generateCursorBlueprints(variables);
    case 'codex':
      return generateCodexBlueprints(variables);
    case 'junie':
      return generateJunieBlueprints(variables);
    case 'factory':
      return generateFactoryBlueprints(variables);
    case 'windsurf':
      return generateWindsurfBlueprints(variables);
    case 'copilot':
      return generateCopilotBlueprints(variables);
    default:
      // IDEs without subagent support get no blueprints
      return [];
  }
}

// ── Claude Code: YAML agent definitions ─────────

function generateClaudeCodeBlueprints(vars: TemplateVariables): GeneratedFile[] {
  return AGENT_ROLES.map((agent) => {
    const content = `# ${agent.title} agent configuration
# Generated by Initra for ${vars.projectName}
type: ${agent.role}
capabilities:
${agent.capabilities.map((c) => `  - ${c}`).join('\n')}
optimizations:
  - context-caching
  - memory-persistence
project:
  name: ${vars.projectName}
  framework: ${vars.framework}
  language: ${vars.language}
`;

    return {
      ideTarget: 'claude-code' as IDETarget,
      filename: `${agent.role}.yaml`,
      filePath: `.claude/agents/${agent.role}.yaml`,
      content,
    };
  });
}

