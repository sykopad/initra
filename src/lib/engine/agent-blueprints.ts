// ── Codex: TOML agent definitions ───────────────

function generateCodexBlueprints(vars: TemplateVariables): GeneratedFile[] {
  return AGENT_ROLES.map((agent) => {
    const toolMap: Record<AgentRole, string[]> = {
      architect: ['read', 'write', 'edit', 'glob', 'grep'],
      coder: ['read', 'write', 'edit', 'bash', 'glob', 'grep'],
      tester: ['read', 'write', 'edit', 'bash', 'grep'],
      reviewer: ['read', 'glob', 'grep'],
      researcher: ['read', 'glob', 'grep', 'web_search'],
    };

    const modelMap: Record<AgentRole, string> = {
      architect: 'inherit',
      coder: 'fast',
      tester: 'fast',
      reviewer: 'inherit',
      researcher: 'inherit',
    };

    const tools = toolMap[agent.role] || ['read', 'glob', 'grep'];
    const model = modelMap[agent.role] || 'inherit';

    const guardrailLines = agent.guardrails
      .map((g) => g.replace(/"/g, '\\"'))
      .join('\\n');

    const content = `# ${agent.title} agent configuration
# Generated by Initra for ${vars.projectName}

name = "${agent.role}"
description = "${agent.description}"

developer_instructions = """
You are a ${agent.title} for the ${vars.projectName} project.
Framework: ${vars.framework} | Language: ${String(vars.languageLabel || vars.language)}${vars.database ? ` | Database: ${vars.database}` : ''}

Guardrails:
${agent.guardrails.map((g) => `- ${g}`).join('\n')}
"""

[tools]
enabled = [${tools.map((t) => `"${t}"`).join(', ')}]

[model]
preference = "${model}"
`;

    return {
      ideTarget: 'codex' as IDETarget,
      filename: `${agent.role}.toml`,
      filePath: `.codex/agents/${agent.role}.toml`,
      content,
    };
  });
}

